print(saldo)

"atribuindo a divisão arredondada por um novo valor a variável"
saldo //= 2
print(saldo)

"atribuindo a divisão por um novo valor a variável"
saldo /= 2
print(saldo)

"atribuindo a multiplicação por um novo valor a variável"
