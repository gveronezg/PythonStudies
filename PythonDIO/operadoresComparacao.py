"""
O retorno das comparações serão sempre valores booleanos
True: quando a condição for atendida
False: quando a condição não for atendida
"""
saldo = 45
saque = 20

"Confere se as duas variáveis são iguais usando o (==)"
print(saldo == saque)
"Confere se as duas variáveis são diferentes usando o (!=)"
print(saldo != saque)
"Confere se a variavél da esquerda é maior usando o (>)"
print(saldo > saque)
"Confere se a variavél da esquerda é maior ou igual usando o (>=)"
print(saldo >= saque)
"Confere se a variavél da esquerda é menor usando o (<)"
print(saldo < saque)
"Confere se a variavél da esquerda é menor ou igual usando o (<=)"
print(saldo <= saque)
